int	ft_is_prime(int nb)
{
	int	c;

	while (1)
	{
		c = 2;
		if (nb < 2)
			return (0);
		while (c <= nb / 2)
		{
			if (nb % c == 0)
				return (0);
			c++;
		}
		return (1);
	}
}
