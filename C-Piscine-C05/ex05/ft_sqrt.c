int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (n * n != nb)
	{
		n++;
		if (n >= nb)
		{
			return (0);
		}
		else if (n >= 46341)
		{
			return (0);
		}
	}
	return (n);
}
