char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*dst;

	dst = dest;
	while (*dst != '\0')
		dst++;
	while (*src != '\0' && nb > 0)
	{
		*dst = *src;
		dst++;
		src++;
		nb--;
	}
	*dst = '\0';
	return (dest);
}
